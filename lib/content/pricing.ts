export function formatPricingAmount(amount: number | null, locale: Locale) {
  if (amount === null) {
    return getLocalizedText(TEXT.freeLabel, locale);
  }

  return formatCurrency(amount, locale);
}

export function getPricingContent(locale: Locale) {
  return {
    pageTitle: getLocalizedText(TEXT.pageTitle, locale),
    metadataDescription: getLocalizedText(TEXT.heroBody, locale),
    heroTitle: getLocalizedText(TEXT.heroTitle, locale),
    heroBody: getLocalizedText(TEXT.heroBody, locale),
    mostPopular: getLocalizedText(TEXT.mostPopular, locale),
    highlightsTitle: getLocalizedText(TEXT.highlightsTitle, locale),
    highlightsBody: getLocalizedText(TEXT.highlightsBody, locale),
    highlightChips: [
      getLocalizedText(TEXT.highlightClients, locale),
      getLocalizedText(TEXT.highlightTrust, locale),
      getLocalizedText(TEXT.highlightInsights, locale),
    ],
    ctaTitle: getLocalizedText(TEXT.ctaTitle, locale),
    ctaBody: getLocalizedText(TEXT.ctaBody, locale),
    ctaAction: getLocalizedText(TEXT.ctaAction, locale),
    forever: getLocalizedText(TEXT.forever, locale),
    monthSuffix: getLocalizedText(TEXT.monthSuffix, locale),
    schemaName: getLocalizedText(TEXT.pageTitle, locale),
    schemaDescription: getLocalizedText(TEXT.heroBody, locale),
    plans: PLANS.map((plan) => ({
      id: plan.id,
      name: getLocalizedText(plan.name, locale),
      description: getLocalizedText(plan.description, locale),
      price: plan.price,
      periodLabel:
        plan.period === 'forever'
          ? getLocalizedText(TEXT.forever, locale)
          : getLocalizedText(TEXT.monthSuffix, locale),
      cta: getLocalizedText(plan.cta, locale),
      href: plan.href,
      popular: plan.popular,
      features: plan.featureIds.map((featureId) => getLocalizedText(FEATURES[featureId], locale)),
    })),
  };
}
